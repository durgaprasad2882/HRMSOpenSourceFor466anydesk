package hrms.model.parmast;

public class AchievementBean {

    private String achieveId = null;
    private String task = null;
    private String target = null;
    private String achievement = null;
    private String percentQualitative = null;
    private String percentAchievement = null;
    private String slno = null;
    private String attachmentId = null;
    private String attachmentname = null;

    public String getAchieveId() {
        return achieveId;
    }

    public void setAchieveId(String achieveId) {
        this.achieveId = achieveId;
    }

    public String getTask() {
        return task;
    }

    public void setTask(String task) {
        this.task = task;
    }

    public String getTarget() {
        return target;
    }

    public void setTarget(String target) {
        this.target = target;
    }

    public String getAchievement() {
        return achievement;
    }

    public void setAchievement(String achievement) {
        this.achievement = achievement;
    }

    public String getPercentQualitative() {
        return percentQualitative;
    }

    public void setPercentQualitative(String percentQualitative) {
        this.percentQualitative = percentQualitative;
    }

    public String getPercentAchievement() {
        return percentAchievement;
    }

    public void setPercentAchievement(String percentAchievement) {
        this.percentAchievement = percentAchievement;
    }

    public String getSlno() {
        return slno;
    }

    public void setSlno(String slno) {
        this.slno = slno;
    }

    public String getAttachmentId() {
        return attachmentId;
    }

    public void setAttachmentId(String attachmentId) {
        this.attachmentId = attachmentId;
    }

    public String getAttachmentname() {
        return attachmentname;
    }

    public void setAttachmentname(String attachmentname) {
        this.attachmentname = attachmentname;
    }

}
