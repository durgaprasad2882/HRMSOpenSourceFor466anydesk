package hrms.model.common;

public class EmpPersonalInfo {

    String empId = null;
    String gpfNo = null;
    String empName = null;
    String gisno = null;
    String dateofBirth = null;
    String doeGovt = null;
    String dob = null;
    String curPay = null;
    String payScale = null;

    public String getEmpId() {
        return empId;
    }

    public void setEmpId(String empId) {
        this.empId = empId;
    }

    public String getGpfNo() {
        return gpfNo;
    }

    public void setGpfNo(String gpfNo) {
        this.gpfNo = gpfNo;
    }

    public String getEmpName() {
        return empName;
    }

    public void setEmpName(String empName) {
        this.empName = empName;
    }

    public String getGisno() {
        return gisno;
    }

    public void setGisno(String gisno) {
        this.gisno = gisno;
    }

    public String getDateofBirth() {
        return dateofBirth;
    }

    public void setDateofBirth(String dateofBirth) {
        this.dateofBirth = dateofBirth;
    }

    public String getDoeGovt() {
        return doeGovt;
    }

    public void setDoeGovt(String doeGovt) {
        this.doeGovt = doeGovt;
    }

    public String getDob() {
        return dob;
    }

    public void setDob(String dob) {
        this.dob = dob;
    }

    public String getCurPay() {
        return curPay;
    }

    public void setCurPay(String curPay) {
        this.curPay = curPay;
    }

    public String getPayScale() {
        return payScale;
    }

    public void setPayScale(String payScale) {
        this.payScale = payScale;
    }

}
