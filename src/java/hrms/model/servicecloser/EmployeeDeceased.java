package hrms.model.servicecloser;

/**
 *
 * @author Manas
 */
public class EmployeeDeceased {

    private String deceasedDate = null;
    private String deceasedTime = null;
    private String empId = null;
    private String deceasednote = null;
    private String deceasedType = null;
    private String curspc = null;

    public String getDeceasedDate() {
        return deceasedDate;
    }

    public void setDeceasedDate(String deceasedDate) {
        this.deceasedDate = deceasedDate;
    }

    public String getDeceasedTime() {
        return deceasedTime;
    }

    public void setDeceasedTime(String deceasedTime) {
        this.deceasedTime = deceasedTime;
    }

    public String getEmpId() {
        return empId;
    }

    public void setEmpId(String empId) {
        this.empId = empId;
    }

    public String getDeceasednote() {
        return deceasednote;
    }

    public void setDeceasednote(String deceasednote) {
        this.deceasednote = deceasednote;
    }

    public String getDeceasedType() {
        return deceasedType;
    }

    public void setDeceasedType(String deceasedType) {
        this.deceasedType = deceasedType;
    }

    public String getCurspc() {
        return curspc;
    }

    public void setCurspc(String curspc) {
        this.curspc = curspc;
    }
    
    
}
