package hrms.model.cmgipt;

public class CmgiPtBean {

    public String empName = null;
    public String empDesig = null;
    public String empGrossSal = null;
    public String empTaxOnProffesion = null;
    public String remark = null;
    public String totalGross = null;
    public String totalTax = null;

    public String getEmpName() {
        return empName;
    }

    public void setEmpName(String empName) {
        this.empName = empName;
    }

    public String getEmpDesig() {
        return empDesig;
    }

    public void setEmpDesig(String empDesig) {
        this.empDesig = empDesig;
    }

    public String getEmpGrossSal() {
        return empGrossSal;
    }

    public void setEmpGrossSal(String empGrossSal) {
        this.empGrossSal = empGrossSal;
    }

    public String getEmpTaxOnProffesion() {
        return empTaxOnProffesion;
    }

    public void setEmpTaxOnProffesion(String empTaxOnProffesion) {
        this.empTaxOnProffesion = empTaxOnProffesion;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }

    public String getTotalGross() {
        return totalGross;
    }

    public void setTotalGross(String totalGross) {
        this.totalGross = totalGross;
    }

    public String getTotalTax() {
        return totalTax;
    }

    public void setTotalTax(String totalTax) {
        this.totalTax = totalTax;
    }

}
